#addition, division, subtraction, multiplication

def addition(num1,num2):
    return num1 +num2

def subtraction(num1,num2):
    return num1 - num2
def multiplication(num1,num2):
    return num1  * num2
def division(num1,num2):
    return num1 //num2

print("Addtion of given two numbers is :",addition(5,3))
print("multiplication of given two numbers is :",multiplication(2,3))
print("subtraction of given two numbers is :",subtraction(4,3))
print("division of given two numbers is :",division(6,3))